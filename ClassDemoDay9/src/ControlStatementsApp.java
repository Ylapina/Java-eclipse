
public class ControlStatementsApp {

	public static void main(String[] args) {
		int sum = 0;
		//incrementing i by 1
		for(int i= 1; i <= 10; i++) {
			System.out.println("i = "+i+", sum = " + sum);
			sum+= i;
		}
		
		for(int i= 1; i <= 10; i+=2
				
				) {
			System.out.println("i = "+i+", sum = " + sum);
			sum+= i;
		}
	}

}
