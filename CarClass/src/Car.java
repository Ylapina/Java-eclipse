
public class Car {
 private String brand;
 private String model;
 private int numOfDoors;

 
 public Car(){
 
}
public Car(String carBrand, String CarModel, int Doors) {
	

     brand = carBrand;
     model = CarModel;
     numOfDoors = Doors;
}
public int getSpeed() {
       return getSpeed();
}

	


 public String toString() {
	 return
			 "Brand: "+ brand + "model "+ model+ "Doors: "+ numOfDoors; 
 }
 }